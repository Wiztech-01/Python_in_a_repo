# Variables are used to store information to be referenced and manipulated in a computer program.
# They also provide a way of labeling data with a descriptive name, so our programs can be understood more clearly by the reader and ourselves. 
# It is helpful to think of variables as containers that hold information. 
# Their sole purpose is to label and store data in memory. This data can then be used throughout your program.


# The variable box now holds the value "balloons"
box = "balloons"


# The value of the variable box is printed to the console.
print(box)


# The value of the variable box is changed to "biscuits"
box = "biscuits"

# The value of the variable box is printed to the console.
print(box)


# The value of the variable box is changed to "hats"
box = "hats"

# The value of the variable box is printed to the console.
print(box)


# From the above code, we can see that the variable box is being used to store different values.
# The value of the variable box is changed multiple times.
# The value of the variable box is printed to the console after each change.
# The variable box is used to store different values at different times.